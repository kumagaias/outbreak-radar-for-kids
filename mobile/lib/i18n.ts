export type I18nStrings = typeof strings.JP;

export function getDeviceLanguage(): Country {
  // Get device locale (e.g., "ja-JP", "en-US")
  const locale = Localization.getLocales()[0];
  const languageCode = locale?.languageCode || "en";
  
  // Map language code to Country
  if (languageCode === "ja") {
    return "JP";
  }
  return "US"; // Default to US for all other languages
}

export function t(country?: Country): I18nStrings {
  const targetCountry = country || getDeviceLanguage();
  return strings[targetCountry] as I18nStrings;
}
